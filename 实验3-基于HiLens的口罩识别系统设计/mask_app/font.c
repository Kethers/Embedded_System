static void fontDraw(font_t *pf, char *ch, int x, int y,void (*df)(int,int,int))
{
	for (int i=pf->height; i>0; i--) {
		for (int j=pf->width; j>0; j--) {
			if (pf->getCharPoint!=NULL) {
				int st = pf->getCharPoint(ch, j, i);
				df(96-x-j, 32-y-i, st);
			}
		}
	}
}

void fontShow12(int x, int y, char* str, void (*df)(int,int,int))
{
	while (*str != '\0'){
		char ch = *str;
		if ((ch & 0x80) != 0) {
			fontDraw(&fontHZK12, str, x, y, df);
			x += fontHZK12.width;
			str += fontHZK12.codeSize;
		} else {
			fontDraw(&fontAscii6x12, str, x, y, df);
			x += fontAscii6x12.width;
			str += fontAscii6x12.codeSize;
		}
	}
}
void fontShow16(int x, int y, char* str, void (*df)(int,int,int))
{
	while (*str != '\0'){
		char ch = *str;
		//an ASCII code takes low-7-bits of a byte, so use this feature
		//to judge whether it's a CN-character or a EN-character
		if ((ch & 0x80) != 0) {
			fontDraw(&fontHZK16, str, x, y, df);
			x += fontHZK16.width;
			str += fontHZK16.codeSize;
		} else {
			fontDraw(&fontAscii8x16, str, x, y, df);
			x += fontAscii8x16.width;
			str += fontAscii8x16.codeSize;
		}
	}
}

