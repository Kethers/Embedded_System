static int _getASCII6x12CharPoint(char* code, int x, int y)
{
	char c = *code;
	int idx;
	if (c <= ' ') return 0;
	idx = (c - ' ') * 12;
	if (x<6 && y<12) {
		return ASCII6x12[idx+(y/8*6)+x]&(1<<(y%8));
	}
	return 0;
}
static int _getASCII8x16CharPoint(char* code, int x, int y)
{
	char c = *code;
	int idx;
	if (c <= ' ') return 0;
	idx = (c - ' ') * 16;
	if (x<8 && y<16) {
		return ASCII8x16[idx+(y/8*8)+x]&(1<<(y%8));
	}
	return 0;
}
